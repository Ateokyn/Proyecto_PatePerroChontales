    private void btnAgregarParticipanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarParticipanteActionPerformed
        // TODO add your handling code here:
        String nomb1 = jtxtNombreCliente1.getText();
        String nomb2 = jtxtNombreCliente2.getText();
        String ape1 = jtxtApellido1Cliente.getText();
        String ape2 = jtxtApellido2Cliente.getText();
        String edad = jtxtEdadCliente.getText();
        String depart = jtxtDepartamentoCliente.getText();

        if (nomb1.contentEquals("") || nomb2.contentEquals("")|| ape1.contentEquals("") || ape2.contentEquals("")
                || edad.contentEquals("") || depart.contentEquals("")) {
            JOptionPane.showMessageDialog(rootPane, "Todos los campos son obligatorios.");
        } else {
            try {
                Date ed = Date.valueOf(edad);
                Cliente clt = new DAOCLIENTE().Insertar(nomb1, nomb2, ape1, ape2, ed, depart);
                JOptionPane.showMessageDialog(rootPane, "Registro agregado.");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(rootPane, "No se agrego el registro.");
            }
        }
        obtenerDatos();
        LimpiarCampos();
    }//GEN-LAST:event_btnAgregarParticipanteActionPerformed

    private void btnEditarParticipanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarParticipanteActionPerformed
        // TODO add your handling code here:
        int fila = this.jtableCliente.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un registro de la tabla.");
        } else {
            try {
                int id = Integer.parseInt((String) this.jtableCliente.getValueAt(fila, 0).toString());
                String nom1 = (String) this.jtableCliente.getValueAt(fila, 1);
                String nom2 = (String) this.jtableCliente.getValueAt(fila, 2);
                String ap1 = (String) this.jtableCliente.getValueAt(fila, 3);
                String ap2 = (String) this.jtableCliente.getValueAt(fila, 4);
                Date edad = Date.valueOf((String) this.jtableCliente.getValueAt(fila, 5).toString());
                String depart = (String) this.jtableCliente.getValueAt(fila, 6);

                jtxtIdCliente.setText("" + id);
                jtxtNombreCliente1.setText(nom1);
                jtxtNombreCliente2.setText(nom2);
                jtxtApellido1Cliente.setText(ap1);
                jtxtApellido2Cliente.setText(ap2);
                jtxtEdadCliente.setText(String.valueOf(edad));
                jtxtDepartamentoCliente.setText(depart);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnEditarParticipanteActionPerformed

