    public void obtenerDatos() {

        List<Cliente> cliente = new DAOCLIENTE().ObtenerDatos();
        
        DefaultTableModel modelo = new DefaultTableModel();

        String[] columnas = {"id_Cliente", "Primer Nombre", "Segundo Nombre", "Apellido Paterno", "Apellido Materno", "Fecha Nac", "Departamento"};

        modelo.setColumnIdentifiers(columnas);

        for (Cliente cln : cliente) {
            String[] renglon = {Integer.toString(cln.getID_CLIENTE()),
                cln.getNOMBRE_C1(),cln.getNOMBRE_C2(), cln.getAPELLIDO_C1(), cln.getAPELLIDO_C2(),
                cln.getFECHANAC_C().toString(),
                cln.getDEPART_C()};
            modelo.addRow(renglon);
        }
        jtableCliente.setModel(modelo);
    }

    public void actualizarCliente() {

        int id = Integer.parseInt(this.jtxtIdCliente.getText());
        String nom1 = this.jtxtNombreCliente1.getText();
        String nom2 = this.jtxtNombreCliente2.getText();
        String ape1 = this.jtxtApellido1Cliente.getText();
        String ape2 = this.jtxtApellido2Cliente.getText();
        Date edad = Date.valueOf(this.jtxtEdadCliente.getText());
        String depa = this.jtxtDepartamentoCliente.getText();

        DAOCLIENTE dao = new DAOCLIENTE();

        int res = dao.Actualizar(id, nom1, nom2, ape1, ape2, edad, depa);

        if (res == 1) {
            JOptionPane.showMessageDialog(rootPane, "Datos del cliente actualizados.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Lo sentimos. Ocurrio un ERROR.");
        }
    }

